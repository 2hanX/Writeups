# coding=utf-8
import gmpy2
import binascii


n = gmpy2.mpz(
    57772961349879658023983283615621490728299498090674385733830087914838280699121)
e = gmpy2.mpz(65537)
c = gmpy2.mpz(
    36913885366666102438288732953977798352561146298725524881805840497762448828130)

# website: http://www.factordb.com/ to decompose N
p = gmpy2.mpz(202049603951664548551555274464815496697)
q = gmpy2.mpz(285934543893985722871321330457714807993)

d = gmpy2.invert(e, (p - 1) * (q - 1))
m = pow(c, d, n)
print(binascii.unhexlify(hex(m)[2:]))
